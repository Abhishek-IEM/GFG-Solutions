class Solution {
  public:
    int getSingle(vector<int> &arr) {
        // code here
        int n = arr.size();
        
        unordered_map<int, int> mp;
        for(int num : arr) mp[num]++;
        
        for(auto x : mp)
        {
            int a = x.first, b = x.second;
            if(b == 1) return a;
        }
        
        return -1;
    }
};


